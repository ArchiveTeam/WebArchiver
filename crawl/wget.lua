local found_urls_file = os.getenv('FOUND_URLS_FILE')

local url_count = 0
local tries = 0
local final_url = nil
local newurls = {}

read_file = function(file)
  if file then
    local f = assert(io.open(file))
    local data = f:read("*all")
    f:close()
    return data
  else
    return ""
  end
end

file_exists = function(path)
  local f = io.open(path, "rb")
  if f then
    f:close()
  end
  return f ~= nil
end

